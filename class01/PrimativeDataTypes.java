package com.syntax.class01;

public class PrimativeDataTypes {

	public static void main (String []args) {
		
		//8 primative data types
		//to store whole number --> byte,short, int, long
		
		byte variableOne=10;
		
		short variable2=100;
		
		int variableThree= 1000;
		
		long variable_four=10000000;
		
		//to store decimal numbers --> float, double
		
		float variable$Five=12.99f;
		double anyName=132.89;
		
		//to store a single character --> char
		
		char singleValue='F';
		char anotherVariable='%';
		
		//to store boolean values --> boolean
		
		boolean hungry=true;
		boolean thirsty=false;
		
		System.out.println(variable_four);
				
		
		
	}
	
	

}
